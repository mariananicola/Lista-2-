nota = float(input("Informe sua nota: "))

#Nota ≥ 90: "Conceito A"
#Nota ≥ 80: "Conceito B"
#Nota ≥ 70: "Conceito C"
#Abaixo de 70: "Conceito D"

if nota >=90:
    print("Conceito A")
elif nota >=80:
    print("Conceito B")
elif nota >=70:
    print("Conceito C")
else:
    print("Conceito D")